# Probability
# Probability mass functions are just a distribution of all the frequencies of particular outcomes.
# The scipy.stats package has a pmf() function to show certain distributions.
# E.g. to simulate a binomial distribution pmf --> pmf1 = stats.binom.pmf(x, n, p)
# x = number of desired outcomes (e.g. heads)
# n = number of times the event is repeated.
# p = the probability of the desired outcome.
